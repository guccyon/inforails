SELECT 
	 TO_ADDR
	,TO_NAME
	,TO_TYPE
FROM
	T_MAIL_ADDRESS
WHERE
	MAIL_ID = ?
	AND
	SOSHIN_DATE <= SYSDATE
	AND
	SOSHIN_FLG = 0